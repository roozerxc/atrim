#include "impl/MutexSDL.h"

#if USE_SDL2
#include "SDL2/SDL.h"
#else
#include "SDL/SDL.h"
#endif

namespace hpl
{

//////////////////////////////////////////////////////////////////////////
// CONSTRUCTORS
//////////////////////////////////////////////////////////////////////////

//-----------------------------------------------------------------------

cMutexSDL::cMutexSDL()
{
    mpMutexHandle = NULL;
    mpMutexHandle = SDL_CreateMutex();
}

//-----------------------------------------------------------------------

cMutexSDL::~cMutexSDL()
{
    if(mpMutexHandle)
        SDL_DestroyMutex(mpMutexHandle);
}

//-----------------------------------------------------------------------

//////////////////////////////////////////////////////////////////////////
// PUBLIC METHODS
//////////////////////////////////////////////////////////////////////////

//-----------------------------------------------------------------------

bool cMutexSDL::Lock()
{
    return SDL_LockMutex(mpMutexHandle)!=-1;
}

bool cMutexSDL::Unlock()
{
    return SDL_UnlockMutex(mpMutexHandle)!=-1;
}

//-----------------------------------------------------------------------

//////////////////////////////////////////////////////////////////////////
// PRIVATE METHODS
//////////////////////////////////////////////////////////////////////////

//-----------------------------------------------------------------------


//-----------------------------------------------------------------------

}

